class Solution {
  public:
    int canReach(int nums[], int N) {
     
     int ind=N-1;
     for(int i=N-1;i>=0;i--){
         if(nums[i]+i>=ind){
             ind=i;
         }
         
     }
     if(ind==0)return true;
     return false;
     
    }
};

